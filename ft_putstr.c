#include "ft_printf.h"

int	ft_putstr(char *str)
{
	int	i;
	int	len;

	len = 0;
	i = 0;
	if (str == NULL)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (str[i])
	{
		len += ft_putchar(str[i]);
		i++;
	}
	return (i);
}
